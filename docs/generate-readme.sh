## Build from Source

### Prerequisites

- Debian Testing (Trixie) or Ubuntu 24.04+
- `live-build`, `meson`, `cmake`, `ninja-build`
- Qt6 development packages
- wlroots development packages

### Build the ISO

```bash
# Build all components
make build-all

# Build the live ISO (requires root for live-build)
make iso
```

### Test in QEMU

```bash
# Launch in QEMU (interactive)
make run

# Automated testing with desktop screenshot capture
make test-automation
```

### Run Unit Tests

```bash
make test
```

### Create a GitHub Release

```bash
make publish
```

