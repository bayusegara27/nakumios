## Architectural Audit Summary

This section documents the verified architectural vectors per the Lead Systems Architect code audit.

### Vector 1: Compositor Input & Focus Routing
- **xkbcommon keymap**: Compiled via `xkb_keymap_new_from_names()` in `server_new_keyboard()` (`nakumi-wm/src/main.c:310-315`)
- **Keyboard routing**: `keyboard_handle_key()` translates libinput→xkb keysyms, checks Super modifier for compositor bindings, else forwards via `wlr_seat_keyboard_notify_key()` (`main.c:263-289`)
- **Focus switching**: `focus_toplevel()` deactivates previous surface, raises scene node, calls `wlr_seat_keyboard_notify_enter()` with current keycodes+modifiers (`main.c:171-206`)

### Vector 2: Qt6 Wayland Native Extraction
- **Private header**: `#include <QtWaylandClient/private/qwaylandwindow_p.h>` (`layershellwindow.cpp:10`)
- **Surface extraction**: `dynamic_cast<QtWaylandClient::QWaylandWindow*>(m_window->handle())` → `wlSurface()` (`layershellwindow.cpp:110-117`)
- **CMake link**: `Qt6::WaylandClientPrivate` in `target_link_libraries` (`panel/CMakeLists.txt:113`)

### Vector 3: UDisks2 D-Bus Signature
- **Method call**: `QDBusMessage::createMethodCall(UDISKS2_SERVICE, devicePath, UDISKS2_FS, "Mount")` (`mountmanager.cpp:48-49`)
- **a{sv} construction**: `QVariantMap options; msg << QVariant::fromValue(options);` (`mountmanager.cpp:52-53`)
- **Async execution**: `QDBusPendingCallWatcher` on `asyncCall()` result (`mountmanager.cpp:55-59`)

### Vector 4: PTY Render Loop & UI Thread Safety
- **Separate thread**: `PtyReader` runs `poll()` loop in dedicated `QThread` (`terminalbackend.cpp:27-50`)
- **Batched output**: 4096-byte read buffer, 100ms poll timeout (`terminalbackend.cpp:29,36`)
- **SIGWINCH**: `ioctl(m_masterFd, TIOCSWINSZ, &ws)` in `updateWindowSize()` (`terminalbackend.cpp:212-220`)

### Vector 5: VM Resilience Logic
- **DMI detection**: Reads `/sys/class/dmi/id/product_name` for VirtualBox/VMware/QEMU/KVM/Bochs (`start-nakumi:27-34`)
- **systemd fallback**: `systemd-detect-virt -q` as secondary check (`start-nakumi:35-39`)
- **Software rendering**: Exports `WLR_RENDERER=pixman`, `LIBGL_ALWAYS_SOFTWARE=1`, `WLR_NO_HARDWARE_CURSORS=1` (`start-nakumi:52-57`)

### Vector 6: Automated Screenshot Pipeline
- **Guest script**: `/usr/bin/nakumi-screenshot` — waits for compositor, runs `grim`, saves to 9p virtfs mount
- **Systemd service**: `nakumi-screenshot.service` — oneshot triggered after `graphical.target`
- **Host target**: `make test-automation` — QEMU with 180s timeout, 9p share to `./docs/`, validates screenshot extraction

