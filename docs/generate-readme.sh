OUTPUT="${PROJECT_DIR}/docs/RELEASE_NOTES.md"
README="${PROJECT_DIR}/README.md"

# Start with the header
cat > "${README}" << 'EOF'
# NakumiOS v1.0

A modern, lightweight Linux desktop operating system built on Debian Testing (Trixie).

EOF

# Add screenshot section (conditional on file existence)
if [ -f "${PROJECT_DIR}/docs/desktop-screenshot.png" ]; then
    cat >> "${README}" << 'EOF'
## Desktop Screenshot

![NakumiOS Desktop](docs/desktop-screenshot.png)

EOF
else
    cat >> "${README}" << 'EOF'
## Desktop Screenshot

> **Note:** Desktop screenshot is captured automatically by `make test-automation`,
> which boots the ISO in QEMU headlessly, waits for the Wayland compositor to start,
> and runs `grim` via the `nakumi-screenshot.service` systemd oneshot to extract
> the framebuffer image through a 9p virtfs share.

<!-- Screenshot will appear here when generated by the automated pipeline -->
<!-- ![NakumiOS Desktop](docs/desktop-screenshot.png) -->

EOF
fi

# Add the main content (quoted heredoc - no variable expansion)
cat >> "${README}" << 'EOF'
